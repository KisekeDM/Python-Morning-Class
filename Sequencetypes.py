# Lists
list1 = ["apple", 5, "mango", 10.5]
print(list1)
print(type(list1))
print(list1[2])
print(list1[0])
print(list1[0:2])
print(list1*3)

# Tuples
mytuple = ("Mary", 15, "John")
print(mytuple)
print(mytuple[2])
print(type(mytuple))
print(mytuple*2)

# Dictionaries
devices = {1: "laptop", 2: "phone"}
print(type(devices))
print(devices.keys())
print(devices.values())