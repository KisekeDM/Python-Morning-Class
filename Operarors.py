# Comparison operation
first = 20
second = 5

print(first < second)
print(first > second)
print(first >= second)
print(first <= second)
print(first == second)

# Logical operations
num = 3
print(num<5 and num>1)
print(num<8 or num==5)
print(not(num<8 or num==5))

# Operator precedence (BODMAS)
print(2+18/6*8)
